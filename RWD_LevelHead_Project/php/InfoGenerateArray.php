<?php
include_once "IGenerateArray.php";

class InfoGenerateArray implements IGenerateArray {
    public function generateArray(){}

    public function theinfoGenerateArray($name,$tel,$userPic,$address){
        $arr=array(
            "name"=>$name,
            "tel"=>$tel,
            "userPic"=>$userPic,
            "address"=>$address
        );
        return $arr;
    }
}

